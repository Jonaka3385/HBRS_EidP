public class MessDatum {
    private int u;
    private double t, d;

    public MessDatum(int u, double t, double d) {
        setU(u);
        setT(t);
        setD(d);
    }

    public int getU() {
        return u;
    }

    public void setU(int u) {
        this.u = u;
    }

    public double getT() {
        return t;
    }

    public void setT(double t) {
        this.t = t;
    }

    public double getD() {
        return d;
    }

    public void setD(double d) {
        this.d = d;
    }


}
